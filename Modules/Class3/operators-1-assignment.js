/**
 * Operators in JS:
 * 
 * 1. Assignment Operator (=)
 *  is used to assign/reassign value in a variable.
 * 
 *  Js solves the right-hand side of the assignment-operator, 
 *      and result gets assigned to the variable on the left-hand side of the assignment-operator.
 * 
 */

let wordOfTheDay = 'King';
console.log(`${wordOfTheDay} is the word of the day.`);

let planets = ['Earth', 'Mercury', 'Venus', 'Jupiter', 'Saturn'];
console.log(`\ntypeof planets -> ${typeof planets}`);     // typeof planets -> object
console.log(`planets -> ${planets}`);                   // planets -> ['Earth', 'Mercury', 'Venus', 'Jupiter', 'Saturn']


let result = 10 * 20 * 20 + 4;
console.log(`\nresult = ${result}`);
